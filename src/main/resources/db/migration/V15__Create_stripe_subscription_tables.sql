-- V15: Stripe Subscription tabellen
-- subscription_packages: pakket-definities (STARTER / PROFESSIONAL / PREMIUM)
-- employer_subscriptions: actieve abonnementen per werkgever (UNIQUE per werkgever)
-- stripe_webhook_events:  idempotency-log voor inkomende Stripe webhook events

-- ─── subscription_packages ──────────────────────────────────────────────────
CREATE TABLE subscription_packages (
    id                      BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name                    VARCHAR(50)   NOT NULL,
    display_name            VARCHAR(100)  NOT NULL,
    price_monthly_cents     INTEGER       NOT NULL,
    price_yearly_cents      INTEGER       NOT NULL,
    stripe_price_id_monthly VARCHAR(100),
    stripe_price_id_yearly  VARCHAR(100),
    max_active_vacancies    INTEGER,
    can_view_cv             BOOLEAN       NOT NULL DEFAULT FALSE,
    can_see_applicant_contact BOOLEAN     NOT NULL DEFAULT FALSE,
    is_featured_included    BOOLEAN       NOT NULL DEFAULT FALSE,
    is_active               BOOLEAN       NOT NULL DEFAULT TRUE,
    created_at              TIMESTAMP     NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMP     NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_subscription_packages_name UNIQUE (name)
);

CREATE INDEX idx_subscription_packages_name     ON subscription_packages(name);
CREATE INDEX idx_subscription_packages_is_active ON subscription_packages(is_active);

